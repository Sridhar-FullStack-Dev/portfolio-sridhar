import { TimelineData } from "@/components/ui/timeline-data";
import gsap from "gsap";
import Image from "next/image";
import { useEffect, useRef } from "react";
import SplitType from "split-type";

export default function Timeline() {
  const timeLineTextRef = useRef<HTMLHeadingElement>(null);

  useEffect(() => {
    if (!timeLineTextRef.current) return;

    const split = new SplitType(timeLineTextRef.current, {
      types: "chars",
      absolute: false,
    });

    gsap.fromTo(
      split.chars,
      {
        y: 100,
        opacity: 0,
        rotationX: -90,
      },
      {
        y: 0,
        opacity: 1,
        rotationX: 0,
        duration: 1,
        stagger: 0.02,
        ease: "power1.inOut",
        scrollTrigger: {
          trigger: timeLineTextRef.current,
          start: "top 80%",
          end: "top 20%",
          toggleActions: "play none none reverse",
        },
      }
    );

    return () => split.revert();
  }, []);

  return (
    <div className="container px-8">
      <h1
        ref={timeLineTextRef}
        className="milker-font text-8xl uppercase perspective-1000 bg-clip-text bg-gradient-to-r from-red-600 via-blue-600 to ora"
      >
        Timeline
      </h1>

      <TimelineData data={data} />
    </div>
  );
}

